package co.com.recruitment.test.hays.domain;

public class CallSummary {
	
	private String employeeName;
	private String totalCall;
	private String clientName;
	private String timeConsume;
	
	public String getEmployeeName() {
		return employeeName;
	}
	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}
	public String getTotalCall() {
		return totalCall;
	}
	public void setTotalCall(String totalCall) {
		this.totalCall = totalCall;
	}
	public String getClientName() {
		return clientName;
	}
	public void setClientName(String clientName) {
		this.clientName = clientName;
	}
	public String getTimeConsume() {
		return timeConsume;
	}
	public void setTimeConsume(String timeConsume) {
		this.timeConsume = timeConsume;
	}
	
	
	

}
